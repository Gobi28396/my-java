package org.string;

public class AB69 {
	public static void main(String[] args) {

		int n = 90,l=30;
		for (int i = 30; i <= i; i++) {
			for (int j = 30; j > 0; j--) {

				if ((i + j+l) == n) {
					System.out.println("A=" + i);
					System.out.println("B" + j);
					System.out.println("c" + l);
				}
				if ((i+j+l)==n) {
					System.out.println("A="+j);
					System.out.println("B"+i);
					System.out.println("c" + l);
				}
				
			}

		}

	}
}